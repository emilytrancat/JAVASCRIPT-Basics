const weather = '';

// can change the value to either 'sun', 'rain', 'snow', or any other value (which will equal to the last "else" declared.)

if ( weather === 'sun') {
    console.log("It's sunny, so I'm going swimming.");

} else if ( weather === 'rain') {
    console.log("It's raining, so i will read a book.");

}  else if ( weather === 'snow') {
    console.log("It's snowing, so I'm going skiing.");

} else { 
    console.log("I'm stinky like poopoo.");

}